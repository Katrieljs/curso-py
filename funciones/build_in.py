# encontrando el número mayor de una lista
numeros = [4, 71, 34, 81, 9]
numero_mayor = max(numeros)
print(numero_mayor)

# encontrando el número menor de una lista
numero_menor = min(numeros)
print(numero_menor)

# redondear a 6 decimales, el segundo parametro de round es el numero de decimales que queremos, por defecto es 0
numero = round(23.223425, 2)
print(numero)

# retorna False -> 0, vacio, Flase, ninguno / True -> distindo de 0, True, datos
res_bol = bool(10)
print(res_bol)

# retorna True si todos los valores son verdaderos
res_all = all([234, "lalala", True])

# suma todos los valores de un iterable
suma=sum([1,5,7])
print(suma)